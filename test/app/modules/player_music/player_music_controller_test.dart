import 'package:flutter_modular/flutter_modular_test.dart';
import 'package:flutter_test/flutter_test.dart';
import 'package:flutter_modular/flutter_modular.dart';

import 'package:flutter_player_music/app/modules/player_music/player_music_controller.dart';
import 'package:flutter_player_music/app/modules/player_music/player_music_module.dart';

void main() {
  initModule(PlayerMusicModule());
  PlayerMusicController playermusic;

  setUp(() {
    playermusic = PlayerMusicModule.to.get<PlayerMusicController>();
  });

  group('PlayerMusicController Test', () {
    test("First Test", () {
      expect(playermusic, isInstanceOf<PlayerMusicController>());
    });

    test("Set Value", () {
      expect(playermusic.value, equals(0));
      playermusic.increment();
      expect(playermusic.value, equals(1));
    });
  });
}
